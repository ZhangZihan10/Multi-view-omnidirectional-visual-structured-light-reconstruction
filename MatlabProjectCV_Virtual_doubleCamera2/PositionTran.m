function  output=PositionTran(x2,y2)
A1=[0 0 -1 11;0 1 0 4.5; 1 0 0 3];%cv系统转unity世界坐标系，第3列为CV系统的坐标系
A2=[0 0 2.2338 14.635;-2.43 0 0 29.32; 0 1.006 0 -5.01];%unity坐标转真实坐标，与unity中转换公式一致
%cv=[-4.49;-0.51;-7.31;1];

cv=[x2/1000;-0.32;y2/1000;1];
%cv=[-7.31;-0.32;4.48;1];
uv=A1*cv;
uv=[uv;1];
rv=A2*uv;
output=rv;
end